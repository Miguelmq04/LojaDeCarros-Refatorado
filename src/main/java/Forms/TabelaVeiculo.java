    /**
     * Método para preencher a tabela com a lista de objetos do tipo veículo. 
     * Exibe todos os dados de veículo registrados no banco de dados na tabela, preenchendo as colunas 
     * 'ID', 'Marca', 'Modelo', 'Cor, 'Cambio', 'Ano', 'Acéssorios', 'Quilometragem', 'Condição', 'Manutenção', 'Preço', 'Garantia', 
     * 'Registrado por' e 'Data de registro'.
     * 
     * @param veiculo Lista de objetos do tipo veículo a ser exibida na tabela.
     */
    public void preencherTabelaVeiculo(List<Veiculo> veiculo){
        String colunas[] = {"ID","Marca", "Modelo", "Cor", "Cambio", "Ano", "Acéssorios", "Quilometragem", "Condição", "Manutenção", "Preço",
            "Garantia", "Registrado por", "Data de registro"};
        String dados[][] = new String[veiculo.size()][colunas.length];
        
        int i = 0;
        
        for(Veiculo v : veiculo){
            String nomeFuncionario = "";
            String dataRegistro = "";
            
            if(!v.getRegistroVeiculo().isEmpty()){
                RegistroVeiculo registroVeiculo = v.getRegistroVeiculo().get(0);
                nomeFuncionario = registroVeiculo.getFuncionarioId().getNomeFuncionario();
                dataRegistro = registroVeiculo.getDataRegistro().toString();
            }
            
            dados[i] = new String[]{
                String.valueOf(v.getIdVeiculo()),
                v.getMarcaVeiculo(),
                v.getModeloVeiculo(),
                v.getCorVeiculo(),
                v.getCambioVeiculo(),
                String.valueOf(v.getAnoVeiculo()),
                v.getAcessorios(),
                String.valueOf(v.getQuilometragem()),
                v.getCondicaoVeiculo(),
                v.getStatusManutencao(),
                String.valueOf(v.getPrecoVeiculo()),
                v.getGarantia(),
                nomeFuncionario,
                dataRegistro
            };
            i++;
        }
        
        DefaultTableModel model = new DefaultTableModel(dados, colunas);
        tblVeiculo.setModel(model);
    }

