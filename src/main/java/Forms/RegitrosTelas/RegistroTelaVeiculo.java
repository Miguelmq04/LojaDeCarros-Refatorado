    /**
     * Ação responsável por cadastrar um objeto do tipo veículo no banco de dados.
     * Pressionando o botão, ele valida os campos e depois realiza a inserção do veículo no banco de dados.
     * 
     * @param evt Evento disparado pela ação do botão.
     */
    private void btnRegistrarVeiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarVeiculoActionPerformed
        Veiculo veiculo = new Veiculo();
        RegistroVeiculo registroVeiculo = new RegistroVeiculo();
        
        if(!validacaoVeiculo()){
            try{
                veiculo.setMarcaVeiculo(txtMarca.getText());
                veiculo.setModeloVeiculo(txtModelo.getText());
                veiculo.setCorVeiculo(txtCor.getText());
                veiculo.setCambioVeiculo(txtCambio.getText());
                veiculo.setAnoVeiculo(Year.parse(txtAno.getText()));
                veiculo.setAcessorios(txtAcessorios.getText());
                veiculo.setQuilometragem(Double.valueOf(txtQuilometragem.getText()));
                veiculo.setCondicaoVeiculo(txtCondicao.getText());
                veiculo.setStatusManutencao(txtCondicao.getText());
                veiculo.setPrecoVeiculo(Double.valueOf(txtPreco.getText()));
                veiculo.setGarantia(txtGarantia.getText());
                
                Funcionarios FuncionarioSelecionado = (Funcionarios) cbxFuncionario.getSelectedItem();
                registroVeiculo.setFuncionarioId(FuncionarioSelecionado);
                registroVeiculo.setDataRegistro(LocalDate.now());
                
                registroVeiculo.setVeiculos(Arrays.asList(veiculo)); 
                veiculo.setRegistroVeiculo(Arrays.asList(registroVeiculo));
                
                VeiculoDAO veiculoDAO = new VeiculoDAO();
                veiculoDAO.cadastrarVeiculo(veiculo);
                
                JOptionPane.showMessageDialog(rootPane, "O veículo foi registrado.");
                
                txtMarca.setText("");
                txtModelo.setText("");
                txtCor.setText("");
                txtCambio.setText("");
                txtAno.setText("");
                txtAcessorios.setText("");
                txtQuilometragem.setText("");
                txtCondicao.setText("");
                txtManutencao.setText("");
                txtPreco.setText("");
                txtGarantia.setText("");
                cbxFuncionario.setSelectedIndex(0);
            }
            catch(NumberFormatException e){
                JOptionPane.showMessageDialog(rootPane, "Os campos Quilometragem e Preço devem conter apenas números. Por favor, ");
            }
        }
    }//GEN-LAST:event_btnRegistrarVeiculoActionPerformed

