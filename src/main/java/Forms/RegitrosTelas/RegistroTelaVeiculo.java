    /**
     * Válida os campos Marca, Modelo, Cor, Cambio, Ano, Acessórios, Quilometragem, Condição, Manutenção, Preço, Garantia
     * verificando se eles estão no formato correto, verifica se foi a comboBox foi selecionada.
     * 
     * Se os campos não estiverem no formato correto ou se a combobox não foi selecionada, será apresentado uma das mensagens na tela.
     * 
     * @return 'true' se os campos  estiverem corretos e a combobox selecionada., 'false' contrário.
     */
    private boolean validacaoVeiculo(){
        boolean vazio = true;
        
        String anoTexto = txtAno.getText();
        boolean anoValido = anoTexto.matches("\\d{4}");
       
        if(txtMarca.getText().isEmpty() && txtModelo.getText().isEmpty() && txtCor.getText().isEmpty() && txtCambio.getText().isEmpty() &&
                txtAno.getText().isEmpty() && txtAcessorios.getText().isEmpty() && txtQuilometragem.getText().isEmpty() &&
                txtCondicao.getText().isEmpty() && txtManutencao.getText().isEmpty() && txtPreco.getText().isEmpty() &&
                txtGarantia.getText().isEmpty() && cbxFuncionario.getSelectedIndex() == 0){
            
            JOptionPane.showMessageDialog(rootPane, "Você precisa preencher todos os campos, e escolher o funcionário que registrou o veículo.");
        }
        else if(txtMarca.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Você precisa preencher o campo Marca.");
        }
        else if(txtModelo.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Você precisa preencher o campo Modelo.");
        }
        else if(txtCor.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Você precisa preencher o campo Cor.");
        }
        else if(txtCambio.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Você precisa preencher o campo Cambio.");
        }
        else if(txtAno.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Você precisa preencher o campo Ano.");
        }
        else if(txtAcessorios.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Você precisa preencher o campo Acessórios.");
        }
        else if(txtQuilometragem.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Você precisa preencher o campo Quilometragem.");
        }
        else if(txtCondicao.getText().isEmpty()){   
            JOptionPane.showMessageDialog(rootPane, "Você precisa preencher o campo Condição.");
        }
        else if(txtManutencao.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Você precisa preencher o campo Manutenção.");
        }
        else if(txtPreco.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Você precisa preencher o campo Preço.");
        }
        else if(txtGarantia.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Você precisa preencher o campo Garantia.");
        }
        else if(!anoValido){
            JOptionPane.showMessageDialog(rootPane, "O ano inserido é inválido. Por favor, preencha o campo Ano utilizando o formato YYYY.");
        }
        else if(cbxFuncionario.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(rootPane, "Escolha o funcionario que está está fazendo o registro do veículo.");
        }
        else{
            vazio = false;
        }
        
        return vazio;
    }
    
